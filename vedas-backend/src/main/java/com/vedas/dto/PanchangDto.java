package com.vedas.dto;

public class PanchangDto {
    private String dateLabel;
    private String weekday;
    private String tithi;
    private String nakshatra;
    private String yoga;
    private String festival;
    private String note;

    public String getDateLabel() { return dateLabel; }
    public void setDateLabel(String dateLabel) { this.dateLabel = dateLabel; }
    public String getWeekday() { return weekday; }
    public void setWeekday(String weekday) { this.weekday = weekday; }
    public String getTithi() { return tithi; }
    public void setTithi(String tithi) { this.tithi = tithi; }
    public String getNakshatra() { return nakshatra; }
    public void setNakshatra(String nakshatra) { this.nakshatra = nakshatra; }
    public String getYoga() { return yoga; }
    public void setYoga(String yoga) { this.yoga = yoga; }
    public String getFestival() { return festival; }
    public void setFestival(String festival) { this.festival = festival; }
    public String getNote() { return note; }
    public void setNote(String note) { this.note = note; }
}
